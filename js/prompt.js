// ── 분석 기간(analysisRange) 파싱 헬퍼 ──────────────────────────
// value 형식: 'future-5' | 'future-10' | 'future-20' | 'future-all'
//           | 'past-5' | 'past-10' | 'past-20' | 'past-all'
// birthYear: 출생연도(과거 전체 선택 시 시작점으로 사용)
// currentYear: 분석 기준 연도
// 반환: { seunStartYear, seunCount, rangeLabel, isPast }
function parseAnalysisRange(analysisRange, birthYear, currentYear) {
  const [direction, amountStr] = String(analysisRange).split('-');
  const isPast = direction === 'past';
  const isAllRange = amountStr === 'all';

  let seunStartYear, seunCount, rangeLabel;

  if (isPast) {
    if (isAllRange) {
      seunStartYear = birthYear;
      seunCount = Math.max(1, currentYear - birthYear + 1);
      rangeLabel = `과거 전체(${birthYear}년~${currentYear}년)`;
    } else {
      const n = parseInt(amountStr, 10) || 10;
      seunStartYear = currentYear - n;
      seunCount = n + 1; // 시작연도부터 현재까지 포함
      rangeLabel = `과거 ${n}년(${seunStartYear}년~${currentYear}년)`;
    }
  } else {
    if (isAllRange) {
      seunStartYear = currentYear;
      seunCount = Math.max(1, (birthYear + 100) - currentYear); // 100세까지를 평생 범위의 근사 상한으로 사용
      rangeLabel = `향후 전체(평생, ${currentYear}년~${birthYear + 100}년경)`;
    } else {
      const n = parseInt(amountStr, 10) || 10;
      seunStartYear = currentYear;
      seunCount = n;
      rangeLabel = `향후 ${n}년(${currentYear}년~${currentYear + n - 1}년)`;
    }
  }

  return { seunStartYear, seunCount, rangeLabel, isPast };
}

